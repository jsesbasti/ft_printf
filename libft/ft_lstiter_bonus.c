#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	if (!lst)
		return ;
	while (lst)
	{
		aux = lst->next;
		f(lst);
		lst = aux;
	}
}
