int	alnum(int c)
{
	unsigned char	a;

	if (a >= '0' && a <= '9')
		return (1);
	else if ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z'))
		return (1);
	else
		return (0);
}
